// employee class

class Employee {
    constructor(name, id, email) {
    this.name = name;
    this.id = id;
    this.email = email;
    }
}

// return values from inquirer input
const getName = () => this.name;
const getId = () => this.id;
const getEmail = () => this.email;
const getRole = () => 'Employee';

module.exports = Employee